<h1>Smellet</h1>  <h3>by MCMK Inventions</h3>
<p>
The Smellet was the first MCMK invention. It is a small
scented pellet. We have produced many different scents.
We are currently not manufacturing Smellets, but may 
begin soon. We have produced many types of Smellets,
and I will provide links to their pages below. All links now work! I may add pictures of the Smellets.
</p>
<p>
The standard Smellet is not very large, and has one side.
The other side is taped closed. They are made from a 
tissue, and have a scent rubbed or sprayed on. The original
Smellet was a tissue with hand sanitizer on it, but none
are made this way now. Some may have scented hand sanitizers, 
scented lip balm, or room spray. Some scents include
Grape, Cinnamon-Raspberry, Buttercream, Mint, and many others.
