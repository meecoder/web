<h1>
Class Security&reg;
</h1>
<h3>
Class Security provides a service much like an international police force.
Its main goal is to defend against monsters like the Kanglemonster, Paint Monster, and the Arctic and Antarctic Sea Wolves, but it also acts as a standard police force. 
</h3>
<p>
Class Security uses technology such as USBs - United States Bees -  and GSIs - Giant Stinger Insects. In the future, we will post pictures of these. Class Security also uses hover technology and specialized armor. Class Security training is the longest and hardest of training for any Class Services branch. Class Security also has the most agents and worldwide locations, allowing people anywhere in the world access to a nearby Class Security location with agents available - even if they have no local police in the area. Class Security even has bases on Antarctica. For quite a few types of things, Class Security works with the Class Medical Service, or CMS, therefore you may see them together.
</p>
