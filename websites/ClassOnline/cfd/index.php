<h3>
The Class Fire Department is an international fire department.
</h3>
<p>
CFD has many types of fire trucks, including water tank trucks, ladder trucks, airport fire trucks, and fireboats. We deploy these to locations all over the world. These locations are usually near CMS locations, as CFD uses ambulances at most fires. To become a CFD firefighter, you will need a commercial driver's license and scientific experience having to do with fire. CFD training consists of putting out a lot of practice fires, and using CMS training. To become a CFD firefighter, you also must have CMS experience to level 15. CFD may also use USBs - United States Bees - or Mini GSIs - not-so-Giant Stinger Insects with fire equipment.
</p>
<p>
If you are a CFD firefighter, there will be a page to access your firefighter portal soon. However, it is not available yet. You will be contacted when it is released.
</p>
