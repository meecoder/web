<h3>
The Class Medical Service runs hospitals and ambulances around the world.
</h3>
<p>
The Class Medical Service runs many hospitals all over the world. They are all run by Class Services, so they use some Class Security technology as well. All CMS doctors are trained by CMS professionals, so you can always be assured that you are getting the best care possible. CMS also runs ambulances to bring people to the hospitals. You can view a picture of one below. These ambulances have the Class Services blue stripe. If you would like to be a CMS doctor, you might want to get CFD training too, and CMS is on-scene at most fires. CMS ambulance drivers also must pass some medical training in case of an emergency. CMS drivers must have a commercial driver's license.
</p>
